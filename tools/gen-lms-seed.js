/* Piano Lab — generate the LMS course seed SQL from the live curriculum, so the
   database rows can never drift from PLLessons. Run under Electron-as-node:
     ELECTRON_RUN_AS_NODE=1 <electron> tools/gen-lms-seed.js
   Emits supabase/01-course-seed.sql (course + units + one item per lesson). */
const fs=require("fs"),vm=require("vm"),path=require("path");
const root=path.join(__dirname,"..");
function load(f){ vm.runInThisContext(fs.readFileSync(path.join(root,f),"utf8"),{filename:f}); }
["js/config.js","locales/en.js","js/i18n.js","js/lessons.js"].forEach(load);
const t=PLI18N.t;

let sql="-- Piano Lab curriculum seed — GENERATED by tools/gen-lms-seed.js. Do not edit by hand.\n"+
        "-- Additive: touches only the 'piano-lab' course; existing courses are untouched.\n"+
        "insert into courses(id,title,active) values ('piano-lab','Piano Lab',true)\n"+
        "  on conflict (id) do update set title=excluded.title;\n";

PLLessons.units().forEach(u=>{
  sql+="insert into curriculum_units(course_id,unit,title) values ('piano-lab',"+u.unit+
       ",$q$"+t(u.nameKey)+"$q$) on conflict (course_id,unit) do update set title=excluded.title;\n";
});

PLLessons.list().forEach((l,i)=>{
  const item=i+1, lbd=JSON.stringify(l.exercises);
  sql+="insert into curriculum_items(course_id,item_id,unit,label,title,route,lbd_ids,lbd_count,has_quiz,status,version) values "+
       "('piano-lab',"+item+","+l.unit+",$q$"+l.label+"$q$,$q$"+t(l.titleKey)+"$q$,"+
       "$q$app.html?lesson="+l.id+"$q$,'"+lbd+"'::jsonb,"+l.exercises.length+",false,'active',1) "+
       "on conflict (course_id,item_id) do update set unit=excluded.unit,label=excluded.label,"+
       "title=excluded.title,route=excluded.route,lbd_ids=excluded.lbd_ids,lbd_count=excluded.lbd_count,"+
       "has_quiz=excluded.has_quiz,status=excluded.status,version=excluded.version;\n";
});

fs.mkdirSync(path.join(root,"supabase"),{recursive:true});
fs.writeFileSync(path.join(root,"supabase","01-course-seed.sql"),sql);
console.log("wrote supabase/01-course-seed.sql —",PLLessons.list().length,"items,",
            PLLessons.units().length,"units");
